package com.lowagie.text.pdf.fonts.cmaps;

public class CodespaceRange {
    private byte[] end;
    private byte[] start;

    public byte[] getEnd() {
        return this.end;
    }

    public void setEnd(byte[] bArr) {
        this.end = bArr;
    }

    public byte[] getStart() {
        return this.start;
    }

    public void setStart(byte[] bArr) {
        this.start = bArr;
    }
}
